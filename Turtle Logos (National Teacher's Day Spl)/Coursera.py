#Coursera logo
import turtle
t = turtle.Turtle()
t.speed(5)
turtle.Screen().bgcolor("#98DDFA")
t.color("blue")
t.pensize(10)
t.penup()
t.backward(250)
t.pendown()
t.right(180)
t.circle(25,270)
t.left(180)
t.circle(25,-315)
t.circle(25,315)
t.circle(-25,300)
t.penup()
t.forward(130)
t.left(30)
t.forward(98)
t.write("ursera", move=False, align="center", font=("Century Gothic", 72, "bold"))
turtle.done()